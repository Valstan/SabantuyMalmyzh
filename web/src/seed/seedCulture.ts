// ─── Запуск ───────────────────────────────────────────────────────────────────
const payload = await getPayload({ config })
const log = (...a: unknown[]) => payload.logger.info(a.map(String).join(' '))
const force = process.env.SEED_FORCE === '1'

for (const def of PAGES) {
  const existing = await payload.find({
    collection: 'pages',
    where: { slug: { equals: def.slug } },
    limit: 1,
    overrideAccess: true,
  })
  if (existing.totalDocs > 0) {
    if (!force) {
      log(`= страница «${def.title}» (/${def.slug}) уже есть — пропуск`)
      continue
    }
    await payload.update({
      collection: 'pages',
      id: existing.docs[0].id,
      data: { title: def.title, content: def.body } as any,
      overrideAccess: true,
    })
    log(`↻ страница «${def.title}» (/${def.slug}) обновлена (SEED_FORCE)`)
    continue
  }
  await payload.create({
    collection: 'pages',
    data: {
      title: def.title,
      slug: def.slug,
      content: def.body,
      _status: 'published',
      publishedAt: def.date,
    } as any,
    overrideAccess: true,
  })
  log(`✓ страница «${def.title}» (/${def.slug})`)
}

