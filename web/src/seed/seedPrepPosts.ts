// ─── Run ──────────────────────────────────────────────────────────────────────
const payload = await getPayload({ config })
const force = process.env.SEED_FORCE === '1'

for (const post of POSTS) {
  const existing = await payload.find({
    collection: 'news',
    where: { slug: { equals: post.slug } },
    limit: 1,
    overrideAccess: true,
  })
  if (existing.totalDocs > 0) {
    if (force) {
      await payload.update({
        collection: 'news',
        id: existing.docs[0].id,
        data: { title: post.title, excerpt: post.excerpt, body: post.body, publishedAt: post.publishedAt } as any,
        locale: 'ru',
        overrideAccess: true,
      })
      log(`↻ /novosti/${post.slug} обновлён (SEED_FORCE)`)
    } else {
      log(`= /novosti/${post.slug} уже есть — не трогаю (правки в /admin сохранены)`)
    }
    continue
  }
  await payload.create({
    collection: 'news',
    data: {
      title: post.title,
      excerpt: post.excerpt,
      body: post.body,
      publishedAt: post.publishedAt,
      slug: post.slug,
      _status: 'published',
    } as any,
    locale: 'ru',
    overrideAccess: true,
  })
  log(`✓ /novosti/${post.slug} создан`)
}

log('Готово: серия «Готовимся к Сабантую».')
process.exit(0)
