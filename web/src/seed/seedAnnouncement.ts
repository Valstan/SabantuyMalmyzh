// ─── Запуск ───────────────────────────────────────────────────────────────────
const payload = await getPayload({ config })
const log = (...a: unknown[]) => payload.logger.info(a.map(String).join(' '))
const force = process.env.SEED_FORCE === '1'

const existing = await payload.find({
  collection: 'pages',
  where: { slug: { equals: SLUG } },
  limit: 1,
  overrideAccess: true,
})

let id: number | string
if (existing.totalDocs > 0) {
  id = existing.docs[0].id
  if (force) {
    await payload.update({
      collection: 'pages',
      id,
      data: { title: TITLE_RU, content: BODY_RU } as any,
      overrideAccess: true,
    })
    log(`↻ ru /${SLUG} обновлена (SEED_FORCE)`)
  } else {
    log(`= /${SLUG} уже есть — ru-тело не трогаю (правки в /admin сохранены)`)
  }
} else {
  const created = await payload.create({
    collection: 'pages',
    data: {
      title: TITLE_RU,
      slug: SLUG,
      content: BODY_RU,
      _status: 'published',
      publishedAt: '2026-06-25T00:00:00.000Z',
    } as any,
    overrideAccess: true,
  })
  id = created.id
  log(`✓ ru /${SLUG} — «${TITLE_RU}»`)
}

