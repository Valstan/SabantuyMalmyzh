// Простой стоп-фильтр мата для UGC (имя автора / подпись). Лёгкий ПРЕД-фильтр, не
// основная защита — главные предохранители постмодерации это «пожаловаться» →
// авто-скрытие на пороге + ручное скрытие в /admin (см. план). Цель здесь — отсечь
// явный мат на входе, не претендуя на полноту.
//
// Реализация — по нормализованным ТОКЕНАМ (границы слов = не-буквы), без regex-
// lookbehind: токен-подход проще и устойчивее к ложным позитивам внутри слов.
// Для характерных корней (хуй/пизд/…) достаточно вхождения в токен; для «еб»-семьи
// требуем НАЧАЛО токена (с приставкой), иначе ловили бы «тр-еб-овать» (ложный позитив).

// ё уже свёрнут в е нормализацией → в корнях пишем только «е».
const SUBSTRING_ROOTS = [
  'хуй',
  'хуе',
  'хуя',
  'хуи',
  'пизд',
  'пезд',
  'бляд',
  'блят',
  'мудак',
  'мудач',
  'залуп',
  'пидор',
  'пидар',
  'пидр',
  'гондон',
  'гандон',
  'дроч',
  'педрил',
]
const PREFIX_ROOTS = [
  'еб',
  'еба',
  'ебу',
  'ебл',
  'ебан',
  'ебе',
  'наеб',
  'выеб',
  'уеб',
  'поеб',
  'доеб',
  'объеб',
  'разъеб',
  'взъеб',
  'съеб',
  'отъеб',
  'подъеб',
]

// Разделитель токенов — всё, что не кириллица/латиница/цифра (ё уже свёрнут в е).
const TOKEN_SPLIT = /[^а-яa-z0-9]+/

function normalize(s: string): string {
  // lower + ё→е + снятие типового цензурного обфусцирования пунктуацией внутри слова.
  return s
    .toLowerCase()
    .replace(/ё/g, 'е')
    .replace(/[.\-_*'`]/g, '')
}

/** Содержит ли текст явный мат (по нормализованным токенам). */
export function containsProfanity(text: string): boolean {
  if (!text) return false
  const tokens = normalize(text).split(TOKEN_SPLIT).filter(Boolean)
  return tokens.some(
    (tok) =>
      SUBSTRING_ROOTS.some((r) => tok.includes(r)) || PREFIX_ROOTS.some((r) => tok.startsWith(r)),
  )
}
